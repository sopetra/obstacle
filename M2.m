function M = M2 ( h, x, E, I)
  
  M = zeros(4);
  M(1,1) = 12;
  M(1,2) = M(2,1) = 6*h;
  M(1,3) = M(3,1) = -12;
  M(1,4) = M(4,1) = 6*h;
  
  M(2,2) = 4*(h^2);
  M(2,3) = M(3,2) = -6*h;
  M(2,4) = M(4,2) = 2*(h^2);
  
  M(3,3) = 12;
  M(3,4) = M(4,3) = -6*h;
  
  M(4,4) = 4*(h^2);
  
  M = (E*I/(h^3)).*M;
end
